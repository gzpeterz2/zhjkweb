package com.hc.web.po;

public class Master {

	private Integer m_id;
	private String m_name;
	private String m_photos;
	private String m_intro;
	private Integer status;
	
	public Integer getM_id() {
		return m_id;
	}
	public void setM_id(Integer m_id) {
		this.m_id = m_id;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	public String getM_photos() {
		return m_photos;
	}
	public void setM_photos(String m_photos) {
		this.m_photos = m_photos;
	}
	public String getM_intro() {
		return m_intro;
	}
	public void setM_intro(String m_intro) {
		this.m_intro = m_intro;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
}
