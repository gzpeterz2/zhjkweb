package com.hc.web.po;

public class Teacher {

	private Integer t_id;
	private String t_name;
	private String t_course;
	private String t_introduction;
	private String t_photos;
	
	public Integer getT_id() {
		return t_id;
	}
	public void setT_id(Integer t_id) {
		this.t_id = t_id;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_course() {
		return t_course;
	}
	public void setT_course(String t_course) {
		this.t_course = t_course;
	}
	public String getT_introduction() {
		return t_introduction;
	}
	public void setT_introduction(String t_introduction) {
		this.t_introduction = t_introduction;
	}
	public String getT_photos() {
		return t_photos;
	}
	public void setT_photos(String t_photos) {
		this.t_photos = t_photos;
	}
	
	
}
